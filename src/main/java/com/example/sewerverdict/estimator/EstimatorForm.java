package com.example.sewerverdict.estimator;

public class EstimatorForm {

	private String role;
	private String location;
	private String houseAgeBand;
	private String issueState;
	private String defectType;
	private String accessType;
	private String urgency;

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getHouseAgeBand() {
		return houseAgeBand;
	}

	public void setHouseAgeBand(String houseAgeBand) {
		this.houseAgeBand = houseAgeBand;
	}

	public String getIssueState() {
		return issueState;
	}

	public void setIssueState(String issueState) {
		this.issueState = issueState;
	}

	public String getDefectType() {
		return defectType;
	}

	public void setDefectType(String defectType) {
		this.defectType = defectType;
	}

	public String getAccessType() {
		return accessType;
	}

	public void setAccessType(String accessType) {
		this.accessType = accessType;
	}

	public String getUrgency() {
		return urgency;
	}

	public void setUrgency(String urgency) {
		this.urgency = urgency;
	}
}
